import numpy as np
def matriz(a,b):
    a = np.array(a,float)
    b = np.array(b,float)
    n = len(b)

    for k in range(n):
        #cambio de lineas
        if np.fabs(a[k,k]) < 1.0e-12:
            for i in range(k+1,n):
                if np.fabs(a[i,k]) > np.fabs(a[k,k]):
                  for j in range(k,n):
                      #intercambio
                      a[k,j],a[i,j]=a[i,j],a[k,j]
                  b[k],b[i] = b[i],b[k]
                  break
        pivote=a[k,k]
        for j in range(k,n):
            a[k,j]/=pivote
        b[k]/= pivote
        for i in range(n):
            if i == k or a[i,k] == 0 :continue
            factor = a[i,k]
            for j in range(k,n):
                a[i,j]-= factor * a[k,j]
            b[i] -= factor *b[k]
    for k in range(n):
        aux=1
        aux*=a[i][k]
    print("determinante",aux)
    return b,a
    

           
